ylim(axes1,[1 100]);
xlabel(axes1,'');
set(axes1,'PlotBoxAspectRatio',[101 101 1],...
    'XColor',[0 0 0],'XGrid','on','XMinorTick','on',...
    'YColor',[0 0 0],'YGrid','on','YMinorTick','on');

% Create textbox 1
annotation(figure1,'textbox',...
    [0.267857142857143 0.528571428571429 0.492857142857143 0.114812891448715],...
    'Color',[1 0 0],...
    'String',{'StiffMa'},...
    'LineStyle','none',...
    'Interpreter','latex',...
    'HorizontalAlignment','center',...
    'FontSize',60,...
    'FitBoxToText','off',...
    'BackgroundColor',[0 1 0]);

