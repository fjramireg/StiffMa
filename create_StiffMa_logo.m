function create_StiffMa_logo
%

% Create figure
figure1 = figure('Color',[1 1 1]);

% Create axes
axes1 = axes('Parent',figure1);

% Create plot
spy(tril(sprand(100,100,0.05)));

% Set axes properties
box(axes1,'on');
